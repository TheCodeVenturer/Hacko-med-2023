const downloadImage = (imageLink) => {
  const canvas = document.createElement("canvas");
  canvas.width = rightHandCanvas.height; // Swap width and height for rotated canvas
  canvas.height = rightHandCanvas.width;
  const context = canvas.getContext("2d");

  let imageSource;
  if (imageLink === "left") {
    imageSource = leftHandCanvas.toDataURL();
  } else {
    imageSource = rightHandCanvas.toDataURL();
  }
  const image = new Image();
  image.src = imageSource;

  image.onload = function () {
    context.translate(canvas.width / 2, canvas.height / 2);
    context.rotate(90 * (Math.PI / 180));
    context.drawImage(
      image,
      -canvas.height / 2,
      -canvas.width / 2,
      canvas.height,
      canvas.width
    );

    const imageToBeDownloaded = new Image();
    imageToBeDownloaded.src = canvas.toDataURL("image/jpeg"); // Set quality to 90%

    const downloadLink = document.createElement("a");
    downloadLink.href = imageToBeDownloaded.src;
    downloadLink.download = `${imageLink}Hand.jpeg`;

    // Trigger a click event on the download link to initiate the download
    downloadLink.click();
  };
};



const inputBox = document.querySelector("#acupuncturePoint");

const handleImageGeneration = () => {
  const { leftHand, rightHand } = keyMap;

  var input = inputBox.value;
  input = input.toLowerCase();
  input = input.split(",");
  for (let item of input) {
    item = item.trim();
    item = item.split(" ")
    let [ele,color] = item;
    color = color=='w'?'white':'yellow'
    if (leftHand[ele]) {
      let [frontX, frontY] = leftHand[ele];
      drawHighlight(ctxLeft, frontX - 1.5, frontY - 1.5,color);
    }
    if (rightHand[ele]) {
      let [frontX, frontY] = rightHand[ele];
      drawHighlight(ctxRight, frontX - 1.5, frontY - 1.5,color);
    }
  }
};





